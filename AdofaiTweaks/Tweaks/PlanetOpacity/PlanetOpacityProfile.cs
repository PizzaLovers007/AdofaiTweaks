namespace AdofaiTweaks.Tweaks.PlanetOpacity;

/// <summary>
/// Structure holding opacity settings for a planet.
/// </summary>
public class PlanetOpacityProfile
{
    /// <summary>
    /// The set body opacity.
    /// </summary>
    public float Body { get; set; }

    /// <summary>
    /// The set tail opacity.
    /// </summary>
    public float Tail { get; set; }

    /// <summary>
    /// The set ring opacity.
    /// </summary>
    public float Ring { get; set; }
}